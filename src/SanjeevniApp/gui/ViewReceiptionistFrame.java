    private void ViewReceptionist()
    {
    try
    {
    ArrayList<EmpPojo> emplist= ReceiptionistDao.getAllReceptionist();
    Object rows[]=new Object[4];
    DefaultTableModel dtm=(DefaultTableModel)jtReceptionistData.getModel();
    for(EmpPojo e:emplist)
    {
    rows[0]=e.getEmpid();
    rows[1]=e.getEmpname();
    rows[2]=e.getJob();
    rows[3]=e.getSal();
    dtm.addRow(rows);
    }
    }
    catch(SQLException ex)
        {
    JOptionPane.showMessageDialog(null,"DbError:"+ex,"Exception",JOptionPane.ERROR_MESSAGE);
    }
 }
   
